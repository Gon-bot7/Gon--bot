import * as ChromeLauncher from 'chrome-launcher';
import * as path from 'path';
import { Browser, Page } from 'puppeteer';
import puppeteer_extra from 'puppeteer-extra';
import { CreateConfig } from '../config/create-config';
import { puppeteerConfig } from '../config/puppeteer.config';
import StealthPlugin = require('puppeteer-extra-plugin-stealth');
import { auth_InjectToken } from './auth';
import { useragentOverride } from '../config/WAuserAgente';
import { checkWebpackJsonp } from './auth';
export async function initWhatsapp(
  session: string,
  options: CreateConfig,
  browser: Browser,
  token?: object
):Promise<Page>{
  const waPage:Page = await getWhatsappPage(browser);
  if (waPage != null) {
    // Auth with token
    await auth_InjectToken(waPage, session, options, token);
    await waPage.setUserAgent(useragentOverride);
    
    const timeout = 2 * 1000;
    await Promise.race([
      waPage.goto(puppeteerConfig.whatsappUrl, { timeout }).catch(() => {}),
      time(waPage,timeout).catch(() => {}),
    ]);

    return waPage;
  }
}
function time(page:Page, time:number){
  if (typeof (page as any).waitForTimeout === 'function') {
    return (page as any).waitForTimeout(time);
  } else {
    return page.waitFor(time);
  }
}
export async function injectApi(page: Page): Promise<Page>{
  return new Promise(async (resolve, reject) => {
    let ch = await checkWebpackJsonp(page).toPromise();
        if(ch){
          try{
              await page.addScriptTag({
                path: require.resolve(
                  path.join(__dirname, '../lib/wapi', 'wapi.js')),
              });
          
              await page.addScriptTag({
                path: require.resolve(
                  path.join(__dirname, '../lib/middleware', 'middleware.js')
                ),
              });
          
              // Make sure WAPI is initialized
              await page.waitForFunction(() => {
                // @ts-ignore
                return !!WAPI.getWAVersion;
              });
              resolve(page);
        }catch{}
    }else{
      reject(false);
    }
  });
}

/**
 * Initializes browser, will try to use chrome as default
 * @param session
 */
export async function initBrowser(
  session: string,
  options: CreateConfig,
  extras = {}
) {
  if (options.useChrome) {
    const chromePath = getChrome();
    if (chromePath) {
      extras = { ...extras, executablePath: chromePath };
    } else {
      console.log('Chrome not found, using chromium');
      extras = {};
    }
  }

  // Use stealth plugin to avoid being detected as a bot
  puppeteer_extra.use(StealthPlugin());

  let browser = null;
  if (options.browserWS && options.browserWS != '') {
    await puppeteer_extra
      .connect({
        browserWSEndpoint: options.browserWS,
      })
      .then((e) => {
        browser = e;
      })
      .catch(() => {
        browser = 'connect';
      });
  } else {
    await puppeteer_extra
      .launch({
        headless: options.headless,
        devtools: options.devtools,
        args: options.browserArgs
          ? options.browserArgs
          : [...puppeteerConfig.chromiumArgs],
        ...options.puppeteerOptions,
        ...extras,
      })
      .then((e) => {
        browser = e;
      })
      .catch(() => {
        browser = 'launch';
      });
  }

  return browser;
}

async function getWhatsappPage(browser: Browser) {
  let pages = null;
  await browser
    .pages()
    .then((e) => {
      console.assert(e.length > 0);
      pages = e[0];
    })
    .catch(() => {});
  return pages;
}

/**
 * Retrieves chrome instance path
 */
function getChrome() {
  try {
    const chromeInstalations = ChromeLauncher.Launcher.getInstallations();
    return chromeInstalations[0];
  } catch (error) {
    return undefined;
  }
}
